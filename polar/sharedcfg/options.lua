PolarConfig.LoadPreviews = true; -- Controls the car previews with the RageUI Garages.
PolarConfig.VehicleStoreRadius = 250; -- Controls radius a vehicle can be stored from.
PolarConfig.AdminCoolDown = false; -- Enables an admin cooldown on call admin.
PolarConfig.AdminCooldownTime = 60; -- 1 minute in (seconds) duration of cooldown. 
